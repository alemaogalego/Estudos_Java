package br.com.exercicios.estudos;

public class Exercicio01Media {
    // Exercício 1: Calculando a média de dois números

    public static void executar() {
        double soma1 = 5;
        double soma2 = 10;
        double media = (soma1 + soma2) / 2.0;
        System.out.println("Exercício 1 - Calculando a média de dois números");
        System.out.println("A média é: " + media);
        System.out.println();
    }
}


